			The NSS-DTU chapter was inaugurated on 13th February 2013 to establish a meaningful linkage between the campus and the community. Therefore, for the national reconstruction and resurgence it was deemed fit that the students and teachers should be properly sensitized and utilized for strengthening the Indian society as a whole with particular emphasis on rural community. 
			The overall aim of NSS-DTU as envisaged earlier, is to give an extended dimension to the higher education system and orient the students towards community service while they are studying in the institution. The reason for the formulation of this is the general realization that the college students have a tendency to get alienated from the village/slum masses which constitute the majority of the population of the country. It is felt that their interaction with the common villagers and slum dwellers will expose the students to the harsh realities of poverty-stricken life and bring about a change in their social perception.
