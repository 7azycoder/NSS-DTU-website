 				Engifest 2015 hosted the sale of these exquisite handicrafts. The kiosk attracted a lot of customers who were amazed to learn that children as young as 4 years of age had put in their creative talent to form the awe-inspiring craftworks; which included colorful wall hangings, earthen vases & pots, baskets, photo frames, bookmarks and earrings. The kiosk was a hit amongst the fest visitors and had a whopping collection of over ₹1,800. This amount was sent as charity to the Emmanuel Seva Group as a goodwill donation by NSS-DTU.

