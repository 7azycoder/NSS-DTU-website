				The workshop intended to bring to fore the innate artistic expressions and creative juices present in children belonging to poor socio-economic strata. It was a delightful experience to watch children play innocently with colors, using their vivid imagination and creative acumen. They keenly noticed the intricacies of handmade craftwork and with the help of the experts turned ordinary looking items into beautiful 'masterpieces'. The kids had great time painting earthen wall hangings, modeling ordinary looking clay to classy earrings, making handmade photo frames using colorful sheets and cutting the mundane A3 sheets to beautiful bookmarks.
